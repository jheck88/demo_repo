package com.tyrannos.data.collector.json;

public class Model {

    public String type = null;
    public String name = null;
    public String mounts = null;
    public String component_size = null;
    public String size = null;
    public String details = null;
    public String quantity = null;
    public String manufacturer = null;
    public String component_class = null;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMounts() {
        return mounts;
    }

    public void setMounts(String mounts) {
        this.mounts = mounts;
    }

    public String getComponent_size() {
        return component_size;
    }

    public void setComponent_size(String component_size) {
        this.component_size = component_size;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getComponent_class() {
        return component_class;
    }

    public void setComponent_class(String component_class) {
        this.component_class = component_class;
    }
}